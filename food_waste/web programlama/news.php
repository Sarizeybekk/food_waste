            <h2>Gıda israfına karşı ortak komite kuruldu</h2>
            <p>Orta Asya ülkeleri, Azerbaycan ve Türkiye, gıda kaybı ve israfını azaltma ve yönetme faaliyetlerinin ülke düzeyinde uygulanmasını, izlenmesini ve diğer paydaşların katılımını kolaylaştırmak amacıyla ortak komite kurduBirleşmiş Milletler Gıda ve Tarım Örgütünden (FAO) yapılan açıklamaya göre, FAO ile Tarım ve Orman Bakanlığı, bölgesel gıda kaybı ve israfını azaltma programının geliştirilmesini ve uygulanmasını denetlemekle görevli ortak bir "Gıda Kaybı ve İsrafı Strateji Komitesi" kurmak için Orta Asya'dan üst düzey yetkililerle toplantı düzenledi.

Kurulan komite, gıda kaybı ve israfını azaltma ve yönetme faaliyetlerinin ülke düzeyinde uygulanmasını destekleyip izleyecek ve diğer ortakların katılımını kolaylaştıracak. Komite, böylelikle, katılımcı ülkeler için FAO ve Türkiye'nin rehberliğinde, iş birliği yapmak ve bilgi paylaşmak için platform işlevi görecek.

Komite, Azerbaycan, Kazakistan, Kırgızistan, Tacikistan, Türkiye, Türkmenistan ve Özbekistan'ı içeren Orta Asya ülkelerinde gıda kaybı ve israfının azaltılmasına yönelik daha geniş bir çabayı temsil eden ve FAO-Türkiye Gıda ve Tarım Ortaklık Programı (FTPP II) çerçevesinde uygulanan FAO inisiyatifinin de parçası olacak.

FAO Orta Asya Alt Bölge Koordinatörü ve Türkiye Temsilcisi Viorel Gutu, toplantının açılışındaki konuşmasında, hükümet temsilcilerinden oluşan komitenin gıda kaybı ve israfını azaltma stratejilerinin geliştirilmesi ve uygulanmasında ulusal sorumluluğu ve sahiplenmeyi güçlendireceğini ve bu çabaların sürdürülebilirliğini sağlayacağını belirtti.</p>
